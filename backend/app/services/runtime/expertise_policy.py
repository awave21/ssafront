"""Shared policy for when expertise/scenario tools should be used.

This module keeps the routing heuristics and human-readable policy text in one
place so runtime descriptions, bridges, and category selection don't drift.
"""

from __future__ import annotations

TACTIC_SITUATIONS_TEXT = (
    "возражение, страх, сомнение, сравнение, эмоциональная реакция, "
    "торг, «подумаю», мягкое закрытие"
)

FACTUAL_REQUESTS_TEXT = (
    "цена, длительность, услуга, сотрудник, филиал, расписание"
)

GREETING_WITHOUT_TOPIC_TEXT = (
    "если клиент написал только приветствие или вежливость без темы"
)


def build_diagnose_tool_description() -> str:
    return (
        "Диагностика сообщения клиента: выбрать мотив и возражения из библиотеки эксперта, "
        "определить стадию воронки и эмоциональное состояние. "
        f"ВЫЗЫВАЙ ПЕРВЫМ (до search_script_flows) если в сообщении клиента есть эмоция/сомнение/возражение и нужен выбор тактики: {TACTIC_SITUATIONS_TEXT}.\n"
        f"НЕ вызывай, {GREETING_WITHOUT_TOPIC_TEXT} "
        "(например только «Добрый день», «Привет», «Здравствуйте» без вопроса про услугу, запись, цену). "
        "В таких случаях ответь текстом без инструментов.\n"
        "Для простых фактических запросов diagnose можно пропустить и сразу вызвать search_script_flows.\n"
        "Правила заполнения:\n"
        " • primary_motive_id — ОБЯЗАТЕЛЬНО заполняй если confidence ≥ 0.6. "
        "Выбирай наиболее близкий id из списка ниже, не null.\n"
        " • emotional_state — выбирай точнее: skeptical при сомнениях/сравнениях, "
        "anxious при страхах/рисках, neutral при нейтральных вопросах.\n"
        " • funnel_stage — consideration если клиент сравнивает или уточняет, "
        "objection если явно возражает, closing если готов записаться.\n"
        "Результат сохраняется как state диалога и влияет на rerank тактик и стиль ответа."
    )


def build_diagnose_prompt_bridge(*, emotional_buffer_states: list[str]) -> str:
    return (
        "У тебя есть инструмент `diagnose_client_motive`. "
        "ИСКЛЮЧЕНИЕ — приветствие без запроса: "
        f"{GREETING_WITHOUT_TOPIC_TEXT} "
        "(«Добрый день», «Привет», «Здравствуйте», «Доброе утро» и т.п.) без темы услуги, "
        "записи, цены, симптомов или возражения — НЕ вызывай diagnose_client_motive и НЕ вызывай "
        "search_script_flows. Ответь коротко и спроси «Чем могу помочь?» / «Что вас интересует?». "
        "Не придумывай тему (например биоревитализацию), пока клиент сам её не сказал.\n"
        "\n"
        "Используй `diagnose_client_motive`, когда нужно явно классифицировать эмоцию/возражение и выбрать ТАКТИКУ разговора: "
        f"{TACTIC_SITUATIONS_TEXT}. "
        f"Для простых factual/operational вопросов ({FACTUAL_REQUESTS_TEXT}) diagnose можно пропустить и перейти сразу к search_script_flows.\n"
        "Если ситуация именно тактическая — сначала вызови `diagnose_client_motive`, затем `search_script_flows`, "
        "чтобы зафиксировать state диалога.\n"
        "Если `diagnose_client_motive` вернул `emotional_state` ∈ "
        f"{{{', '.join(sorted(emotional_buffer_states))}}} и `emotional_pause_used_before=false` — "
        "НЕ вызывай search_script_flows и НЕ начинай продажу. Ответь только эмпатией + "
        "одним открытым вопросом «что именно вас беспокоит / расскажите подробнее». "
        "На следующем ходу переходи к тактике.\n"
        "Если `needs_clarification=true` — задай уточняющий вопрос вместо ответа по тактике."
    )


def build_script_flow_tool_description() -> str:
    return (
        "Поиск экспертных сценариев (потоков) под запрос клиента. "
        "ВЫЗЫВАЙ для любого содержательного сообщения клиента (вопрос/уточнение/возражение/запрос на запись), "
        "чтобы выбрать релевантный сценарий по смыслу и применить тактику ответа. "
        f"Если есть явная эмоция/возражение ({TACTIC_SITUATIONS_TEXT}) — сначала diagnose_client_motive, затем search_script_flows. "
        f"НЕ вызывай, когда {GREETING_WITHOUT_TOPIC_TEXT} (например «Добрый день» — сначала ответь словами и уточни интерес). "
        "Параметр service_id — передавай из sqns_find_booking_options.service_id "
        "если SQNS уже был вызван (улучшает точность сценария под конкретную услугу). "
        "Результат: communication_style (применяй ко всему ответу), "
        "preferred_phrases (используй), forbidden_phrases (не произноси), "
        "required_followup_question (задай дословно в конце ответа)."
    )


def build_script_flows_prompt_bridge() -> str:
    return (
        "У тебя есть инструмент `search_script_flows`. "
        f"НЕ вызывай, если {GREETING_WITHOUT_TOPIC_TEXT} — "
        "сначала кратко ответь и выясни, чем помочь. "
        "Для любого содержательного сообщения клиента вызывай search_script_flows и выбирай сценарий по смыслу запроса. "
        f"Если клиент спрашивает {FACTUAL_REQUESTS_TEXT}, все равно сначала получи сценарий, затем при необходимости дополни factual tools.\n"
        "\n"
        "Результат содержит поля — ИСПОЛЬЗУЙ ИХ ОБЯЗАТЕЛЬНО:\n"
        " • `situation` — точная ситуация клиента из сценария. Сверяйся с ней.\n"
        " • `communication_style` — стиль и тон ВСЕГО твоего ответа. "
        "Если поле непустое — твой ответ ОБЯЗАН соответствовать этому стилю. "
        "Например: «тёплый, 1-2 предложения, один вопрос в конце».\n"
        " • `style_hint` — расшифровка стиля одной фразой.\n"
        " • `preferred_phrases` — используй эти формулировки приоритетно, "
        "адаптируй под контекст.\n"
        " • `forbidden_phrases` — НИКОГДА не произноси эти слова или фразы.\n"
        " • `hard_constraints` — жёсткие запреты из политики клиники. Не нарушай.\n"
        " • `required_followup_question` — если не пусто, ЗАДАЙ ДОСЛОВНО в конце "
        "своего ответа. Не добавляй другие вопросы в этом же сообщении. "
        "Если этот же вопрос уже звучал (см. state_snapshot.asked_followup_questions) "
        "— перефразируй или используй следующий match.\n"
        " • `score` — матчи уже отсортированы с учётом состояния диалога; "
        "используй топ-1, если нет явных причин пропустить.\n"
        "\n"
        "Если текущая реплика клиента задаёт новый фактический или уточняющий вопрос, сначала ответь на него по существу, "
        "а затем мягко верни разговор к следующему шагу сценария. Не игнорируй новый вопрос ради механического следования ветке.\n"
        "Никогда не говори языком системы: не упоминай внутреннюю логику, маршрут, поиск, категории, позиции в системе или регламент.\n"
        "Если `matches` пуст — не выдумывай, отвечай из общего знания и эмпатии.\n"
        "Если `matches` есть — ТВОЙ ОТВЕТ ДОЛЖЕН ТОЧНО СЛЕДОВАТЬ сценарию из топ-1 match."
    )
