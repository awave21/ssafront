"""Шаблон системного промпта нового агента по умолчанию.

В режиме runtime_bridges_mode=manual оркестровочные инструкции должны быть
видны в system_prompt пользователю — они хранятся здесь как единственный источник
правды и подставляются при создании агента если промпт не задан.

При изменении дефолтного поведения новых агентов обновите этот файл.
"""

from __future__ import annotations


_DEFAULT_INTRO = """Ты виртуальный ассистент организации. Отвечай профессионально, кратко и по делу.

Ниже — правила оркестровки инструментов: когда и в каком порядке их вызывать. Следуй им при каждом сообщении клиента."""

_BOOKING_QUALIFICATION_NOTE = (
    "ВАЖНО ПРО ЗАПИСЬ: когда клиент просит записать на услугу, НЕ нужно сразу перечислять всю информацию и все варианты. "
    "Сначала проведи короткую квалификацию: задай 1–2 обязательных уточняющих вопроса (только то, что нужно для корректной записи/подбора специалиста). "
    "После ответов — переходи к подбору через SQNS."
)

_SQNS_SCOPE_NOTE = (
    "Запись через SQNS (ниже): применяй этот блок только если в списке доступных "
    "инструментов есть имена с префиксом sqns_. Если таких инструментов нет — раздел можно игнорировать."
)

_SQNS_IDS_NOTE = (
    "SQNS — числовые ID: resource_id и service_id действительны только в рамках текущего поиска. "
    "При каждом новом запросе на запись заново вызывай sqns_find_booking_options — "
    "не используй resource_id и service_id из памяти без подтверждения текущим вызовом инструмента."
)

_SEPARATOR = "\n\n"


def build_default_agent_system_prompt(*, include_sqns: bool = True) -> str:
    """Полный стартовый системный промпт для нового агента (режим manual)."""
    core = [
        _DEFAULT_INTRO,
        _BOOKING_QUALIFICATION_NOTE,
    ]
    if include_sqns:
        core.append(_SQNS_SCOPE_NOTE)
        core.append(_SQNS_IDS_NOTE)
    return _SEPARATOR.join(p.strip() for p in core if p and str(p).strip())
